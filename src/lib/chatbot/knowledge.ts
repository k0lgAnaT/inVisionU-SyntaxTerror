export function generateResponse(query: string): string {
  const normalizedQuery = query.toLowerCase().trim();
  
  if (!normalizedQuery) return "Я вас не совсем понял. Пожалуйста, напишите свой вопрос!";

  let bestMatch: string = "";
  let highestScore = 0;

  for (const qa of KNOWLEDGE_BASE) {
    let score = 0;
    for (const kw of qa.keywords) {
      if (kw.test(normalizedQuery)) {
        score++;
      }
    }
    
    if (score > highestScore) {
      highestScore = score;
      bestMatch = qa.response;
    }
  }

  if (highestScore > 0) {
    return bestMatch;
  }

  // Fallback response
  return "Извините, я не нашел точного ответа на ваш вопрос. Вы можете узнать про наши гранты, основателя, программы или контакты. Напишите, что вас интересует?";
}
